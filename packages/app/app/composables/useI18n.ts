export function useI18n() {
    const setLanguage = (lang: Language) => {
        currentLanguage.value = lang
        if (process.client) {
            localStorage.setItem('language', lang)
        }
    }

    const t = computed(() => {
        return translations[currentLanguage.value]
    })

    const lang = computed(() => currentLanguage.value)

    return {
        t,
        lang,
        setLanguage,
        currentLanguage,
    }
}
